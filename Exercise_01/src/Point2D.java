
public class Point2D {
	// Attributes
	private int x;
	private int y;
	
	// Constructor
	public Point2D(int x, int y)
	{
		this.x = x;
		this.y = y;
	}

	
	
	// Functions
	
	// Get and Set
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
}
